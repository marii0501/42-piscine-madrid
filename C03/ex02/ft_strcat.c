#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	int	a1;
	int	a2;

	a1 = 0;
	a2 = 0;
	while (dest[a1] != '\0')
		a1++;
	while (src[a2] != '\0')
	{
		dest[a1] = src[a2];
		a1++;
		a2++;
	}
	dest[a1] = '\0';
	return (dest);
}
